            Первая причина – прекращена поддержка Visual Basic на протяжении последних лет. Главная же причина заключается в слабом развитии языка и небольшом наборе возможностей. О чем ещё можно говорить, если в Visual Basic отсутствует полноценное наследование и полиморфизм. Правда, несколько странно нелестно отзываться в отношении похороненного языка.
            Совсем иначе дело обстоит с VB.NET. Он обладает всеми достоинствами платформы .NET и является достойным языком для разработки. Вполне соответствует современным требованиям программирования. Среди существенных минусов: наследование реализовано за счёт механизма имитации, язык работает исключительно на Windows. Также слабая сторона VB.NET - синтаксис. Если учесть, что этот синтаксис языка сильно напоминает C#, которого нет в топе, скорее всего он не является основной причиной ненависти.</p>
