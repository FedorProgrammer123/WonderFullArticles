        <p><b>MATLAB</b> – узкоспециализированный язык, который не используется повсеместно. Большинство разработчиков, работающих на MATLAB – не являются программистами. Нелюбовь к данному языку заключается в том, что разработчики сравнивают тот же Python с MATLAB. </p>
