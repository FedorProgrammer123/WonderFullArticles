            <li>Всё остальное пространство между обеими группами – это люди, которые повсюду заявляют, что у <b>Perl:</b> неудачный синтаксис, кривой ООП, неправильная реализация многопоточности.</li>
        </ol>
        <p>Так и появляется миф, что у языка нет полноценного сообщества.</p>
